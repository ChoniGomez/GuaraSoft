    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        // TODO add your handling code here:
         if(this.jTextFieldIdProyecto.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null, "INGRESE UN CODIGO PARA REALIZAR LA BUSQUEDA", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);JOptionPane.showMessageDialog(null, "INGRESE UN CODIGO PARA REALIZAR LA BUSQUEDA", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
        }
         else{
             List<Proyecto> proyectos = null;
            int idProyecto = Integer.parseInt(this.jTextFieldIdProyecto.getText());// leo y guardo el id de un proyecto
            try {
                proyectos = this.miLogica.dameProyectos();
            } catch (PreexistingEntityException ex) {
                Logger.getLogger(VentanaTurno.class.getName()).log(Level.SEVERE, null, ex);
            }
            for(Proyecto e : proyectos){// recorro la lista de proyectos
                if(e.isId(idProyecto)){// si corresponde al id lo almaceno en unProyecto
                    this.unProyecto = e;
                }
            }
            if(this.unProyecto != null){// verifico si encontro al proyecto
                this.jTextFieldNombreEncargado.setText(this.unProyecto.getUnEncargado().getNombre());// muestro el nombre del encargado
                this.jTextFieldApellidoEncargado.setText(this.unProyecto.getUnEncargado().getApellido());// muestro el apellido del encargado
                this.activarDatosProyecto(false);
                this.activarDatosCuota(true);
                this.jButtonBuscar.setEnabled(false);
                this.jButtonAceptar.setEnabled(true);
                this.jButtonCancelar.setEnabled(true);
            }
            else{// si no encuentro a un cliente con el idProyecto
                JOptionPane.showMessageDialog(null, "NO EXISTE UN PROYECTO CON EL ID: "+idProyecto, "ERROR", JOptionPane.ERROR_MESSAGE);
            }
         }
    }//GEN-LAST:event_jButtonBuscarActionPerformed

