    public void cargarTabla() throws PreexistingEntityException{
        List<Cuota> cuotas = this.miLogica.dameCuotas();
        List<Proyecto> proyectos = this.miLogica.dameProyectos();
        Object[]fila= new Object[6];
        for(Proyecto e : proyectos){
                for(Cuota eq : e.getCuotas()){
                    fila[0] = eq.getId();
                    fila[1] = eq.getMonto();
                    fila[2] = eq.getTipoDeCuota();
                    fila[3] = e.getId();
                    fila[4] = e.getUnEncargado().getNombre();
                    fila[4] = e.getUnEncargado().getApellido();
                    this.modeloEquipo.addRow(fila);
                    this.jTableCuotas.setModel(modeloEquipo);
                }
            }
    }
    
     public void actualizarFilas() throws PreexistingEntityException{
        DefaultTableModel modelo = (DefaultTableModel) jTableCuotas.getModel();
        int filas = jTableCuotas.getRowCount();
        for (int i = 0;filas>i; i++) {
            modelo.removeRow(0);
        }
        cargarTabla();
    }
     
     public void crearCuota(){
        double montoCuota = Double.valueOf(this.jTextFieldMonto.getText());
        String tipoCuota = this.jTextFieldTipo.getText();
        try {
            this.miLogica.crearCuota(this.unProyecto.getId(), montoCuota, tipoCuota);
        } catch (Exception ex) {
            Logger.getLogger(VentanaEquipo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
     
    public void modificarCuota(){
        int idCuota = Integer.parseInt(this.jTextFieldIdCuota.getText());
        int idProyecto = Integer.parseInt(this.jTextFieldIdProyecto.getText());
        double montoCuota = Double.valueOf(this.jTextFieldMonto.getText());
        String tipoCuota = this.jTextFieldTipo.getText();
        try {
            this.miLogica.modificarCuota(idCuota, idProyecto, montoCuota, tipoCuota);
            this.banderaModificar = -1;
        } catch (Exception ex) {
            Logger.getLogger(VentanaEquipo.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
