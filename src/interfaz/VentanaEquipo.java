    public void cargarTabla() throws PreexistingEntityException{
        List<Equipo> equipos = this.miLogica.dameEquipos();
        List<Cliente> clientes = this.miLogica.dameClientes();
        Object[]fila= new Object[5];
        for(Cliente e : clientes){
                for(Equipo eq : e.getEquipos()){
                    fila[0] = eq.getId();
                    fila[1] = eq.getMarca();
                    fila[2] = e.getId();
                    fila[3] = e.getNombre();
                    fila[4] = e.getApellido();
                    this.modeloEquipo.addRow(fila);
                    this.jTableEquipos.setModel(modeloEquipo);
                }
            }
    }
    
    public void actualizarFilas() throws PreexistingEntityException{
        DefaultTableModel modelo = (DefaultTableModel) jTableEquipos.getModel();
        int filas = jTableEquipos.getRowCount();
        for (int i = 0;filas>i; i++) {
            modelo.removeRow(0);
        }
        cargarTabla();
    }
    
    public void crearEquipo(){
        String marcaEquipo = this.jTextFieldMarcaEquipo.getText();
        String anioEquipo = this.jTextFieldAnioEquipo.getText();
        try {
            this.miLogica.crearEquipo(this.unCliente.getId(), marcaEquipo, anioEquipo);
        } catch (Exception ex) {
            Logger.getLogger(VentanaEquipo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void modificarEquipo(){
        int idEquipo = Integer.parseInt(this.jTextFieldIdEquipo.getText());
        int idCliente = Integer.parseInt(this.jTextFieldIdCliente.getText());
        String marcaEquipo = this.jTextFieldMarcaEquipo.getText();
        String anioEquipo = this.jTextFieldAnioEquipo.getText();
        try {
            this.miLogica.modificarEquipo(idEquipo, idCliente, marcaEquipo, anioEquipo);
            this.banderaModificar = -1;
        } catch (Exception ex) {
            Logger.getLogger(VentanaEquipo.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
